// /lib/fortune/prompts/free_report.js

export function promptFreeReport(intake) {
  const p = intake.partner || {};
  const c = intake.concern || {};
  const d = intake.derived || {};
  const ue = d.user_elements || { primary: "火", secondary: "金" };
  const pe = d.partner_elements || { primary: "水", secondary: "木" };

  const userPair = `${ue.primary}×${ue.secondary}`;
  const partnerPair = `${pe.primary}×${pe.secondary}`;

  return [
    "【無料レポート（テキスト出力）】",
    "",
    "■文字数",
    "・日本語で1500〜2000字。",
    "",
    "■出力フォーマット（厳守）",
    "・Markdown記法は禁止（#、##、###、---、>、**、\"\"、`` は使わない）。",
    "・見出しは「■」で統一する。",
    "・箇条書きは「・」で統一する（ハイフン '-' は使わない）。",
    "・空行は段落の切れ目にのみ使用し、詰めすぎない。",
    "・口調は終始「占いばあや」口調の敬語（です・ます）。上から目線禁止。",
    "",
    "■構成（固定）",
    "0) はじめに（寄り添い：2〜3段落）",
    "1) 相性の核：縁の動きと重み（見出し＋3〜4段落）",
    "2) 今週の鍵：心の波紋と分岐点（見出し＋3〜4段落＋分岐は箇条書き可）",
    "3) 有料版のご案内",
    "",
    "■有料版のご案内",
"- 有料版の案内は必ず『下の固定文』をそのまま出力する（言い換え・要約・追記禁止）。",
"- 有料版の案内セクション以外では価格の話をしない。",
"",
"【固定文：ここから】",
"■有料版のご案内",
"この無料版では、縁の流れと分岐点の見立てまでをお伝えいたしました。",
"ただ、結論としての「今、何をするべきか（具体的な一手）」までは、あえて書いておりません。",
"",
"・実践版(480円)：二人の相性をより深掘ります。その上で運勢を読み、今週の一手を1つに決め切ります。コピペで使える送信文テンプレ、禁じ手、心の癖の整え方までをお渡しいたします。",
"・重厚版(980円)：二人の相性を最大限に深掘ります。運勢はこの1ヶ月を週ごとに読み解き、本音が出やすいタイミング、分岐点2つと落とし穴まで含めて、迷いが消える形であなた様の道筋を整えます。",
"",
"いま必要なのが「今週を動かす一手」なら実践版、「1ヶ月の流れごと立て直したい」なら重厚版が向いております。",
"あなた様の縁が静かに整いますように。占いばあやはいつでもあなたの幸せを願っております。",
"【固定文：ここまで】",
"",
    "■0) はじめに（寄り添いの型）",
    "・1段落目：状況を要約し、いまの不安が自然だと肯定する（決めつけない）。",
    "・2段落目：あなた様の性格/気質が『揺れ』を生む理由をやさしく言語化する。",
    "・3段落目：焦らせず安心させたうえで「ここから占いますね」で次へつなぐ。",
    "",
    "■濃さ（重要）",
    "・相性と今週の鍵は浅い一般論にしない。",
    "・心の動きを当てる（バーナム効果）：ユーザー側/相手側ともに“ありがち”を最低3つずつ、自然に散らす。",
    "・返信がない＝脈なしと断言しない。ただし現実的な見立ては必ず入れる。",
    "",
    "■無料版の制約（必ず守る）",
    "・この無料版では、結論としての『具体的な一手（何をいつ送る/どう動く）』は出さない。",
    "・代わりに『関係の流れの見立て』『分岐点の見抜き方（軽め）』までを提示する。",
    "",
 
    "■入力（材料）",
    `本人の気：${userPair}`,
    `相手の気：${partnerPair}`,
    "",
    "本人ミニ鑑定：",
    d.user_mini_reading ?? "",
    "",
    "相手ミニ鑑定：",
    d.partner_mini_reading ?? "",
    "",
    `関係性：${p.relation ?? "不明"}`,
    `直近の出来事：${p.recent_event ?? "不明"}`,
    "",
    "悩み（長文）：",
    c.free_text ?? "",
    "",
    "■出力の最後（必須）",
    "・最後は『あなた様の縁が静かに整いますように。占いばあやはいつでもあなたの幸せを願っております。』のように、短い祈りで締める。",
  ].join("\n");
}
