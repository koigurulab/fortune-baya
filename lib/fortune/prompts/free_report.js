// /lib/fortune/prompts/free_report.js

export function promptFreeReport(intake) {
  const p = intake.partner || {};
  const c = intake.concern || {};
  const d = intake.derived || {};
  const ue = d.user_elements || { primary: "火", secondary: "金" };
  const pe = d.partner_elements || { primary: "水", secondary: "木" };

  const userPair = `${ue.primary}×${ue.secondary}`;
  const partnerPair = `${pe.primary}×${pe.secondary}`;

  return [
    "【無料レポート（テキスト出力）】",
    "",
    "■文字数",
    "・日本語で1500〜2000字。",
    "",
    "■出力フォーマット（厳守）",
    "・Markdown記法は禁止（#、##、###、---、>、**、\"\"、`` は使わない）。",
    "・見出しは「■」で統一する。",
    "・箇条書きは「・」で統一する（ハイフン '-' は使わない）。",
    "・空行は段落の切れ目にのみ使用し、詰めすぎない。",
    "・口調は終始「占いばあや」口調の敬語（です・ます）。上から目線禁止。",
    "",
    "■構成（固定）",
    "0) はじめに（寄り添い：2〜3段落）",
    "1) 相性の核：縁の動きと重み（見出し＋3〜4段落）",
    "2) 今週の鍵：心の波紋と分岐点（見出し＋3〜4段落＋分岐は箇条書き可）",
    "3) 有料版のご案内",
    "",
    "■有料版のご案内",
"- 有料版の案内は必ず『下の固定文』をそのまま出力する（言い換え・要約・追記禁止）。",
"- 有料版の案内セクション以外では価格の話をしない。",
"",
"【固定文：ここから】",
"■有料版のご案内",
"この無料版では、縁の流れと分岐点の見立てまでをお伝えいたしました。",
"ただ、結論としての「今、何をするべきか（具体的な一手）」までは、あえて書いておりません。",
"",
"・今週の整え版(480円)：二人の相性をより深掘ります。その上で運勢を読み、今週の一手を1つに決め切ります。コピペで使える送信文テンプレ、禁じ手、心の癖の整え方までをお渡しいたします。",
"・実践版(980円)：二人の相性を最大限に深掘ります。運勢はこの1ヶ月を週ごとに読み解き、本音が出やすいタイミング、分岐点2つと落とし穴まで含めて、迷いが消える形であなた様の道筋を整えます。",
"・全解読版(1980円)：相性・心理・シナリオ・ロードマップ・禁じ手・処方箋を全方位カバーいたします。3つの道（動く／待つ／離れる）それぞれの具体策とLINEテンプレ付き。「もう迷わない」と思える鑑定をお届けいたします。",
"",
"いま必要なのが「今週を動かす一手」なら整え版、「1ヶ月の流れごと立て直したい」なら実践版、「迷いを全部消したい」なら全解読版が向いております。",
"あなた様の縁が静かに整いますように。占いばあやはいつでもあなたの幸せを願っております。",
"【固定文：ここまで】",
"",
    "■0) はじめに（寄り添いの型）",
    "・1段落目：状況を要約し、いまの不安が自然だと肯定する（決めつけない）。",
    "・2段落目：あなた様の性格/気質が『揺れ』を生む理由をやさしく言語化する。",
    "・3段落目：焦らせず安心させたうえで「ここから占いますね」で次へつなぐ。",
    "",
 "■相性/流れセクションの品質要件（最重要）",
"- 「■相性の核」と「■直近の流れ（7日〜10日程度）」は、浅い一般論を禁止する。必ず“状況のクセ”まで踏み込む。",
"- 文章は“占いとしての語り”。恋愛コーチ/説教/正論の押し付けは禁止。",
"",
"■相性の核：必須描写（一般化・自然に散らす）",
"- ユーザー側の心の揺れを必ず言語化する：平気なふり→確認してしまう→解釈が増える→自己評価が上下する→答え合わせをしたくなる、の流れをどこかに入れる。",
"- 相手側の“止まり方/遅れ方”を必ず言語化する：返したい気持ちはある→返事や反応が関係を動かす重みに感じる→丁寧に向き合うほど負担になる→結果として先送り/間が空く、の流れをどこかに入れる。",
"- 相手の心理は「好き/嫌い」一本化を禁止。「迷い/慎重さ/生活の波/余白を求める」を最低2つ混ぜる。",
"- ユーザーの心理は「不安」一本化を禁止。「期待/怖さ/プライド/踏み込みたい衝動/引きたい衝動」を最低2つ混ぜる。",
"- 入力の固有事実を最低3点本文に入れる（関係性、会った回数や期間、直近の出来事、連絡状況、距離感の変化など）。数値（回数/日数/期間）があれば最低1つ入れる。",
"",
"■今週の鍵（7日〜10日）：必須描写（未来の断言はしない）",
"- 直近は“心の変化”として語る。結果（連絡が来る等）を断言せず、起きやすい内面の動きを2〜3パターンで描写する。",
"- 時間の流れを入れる：前半（数日）は揺れや迷いが増えやすい、後半（1週間前後）は軽い再接続/現状維持/距離の固定化、のように分岐で示す。",
"- 分岐は箇条書き「・」で3つまで。各項目は1〜2文で終える（長い説明は禁止）。",
"- 現実的な見立ては必ず出す（希望だけで濁さない）。ただし断言は避け、可能性の濃淡で表現する。",
"",
"■バーナム効果（一般化ルール）",
"- ユーザー側：『確認してしまう/意味づけしてしまう/気持ちが揺れる/強がる/後で反省する』のうち最低3つを自然に散らす。",
"- 相手側：『慎重/余白が必要/気分や疲れで波が出る/決めるのが重い/向き合うほど遅くなる』のうち最低3つを自然に散らす。",
"",
"■文体ルール",
"- Markdown記法は禁止（#、##、###、---、>、**、\"\"、``` は使わない）。",
"- 見出しは「■」で統一し、箇条書きは「・」で統一する（ハイフン '-' は使わない）。",
"- 空行は段落の切れ目にのみ使い、1文ごとの改行はしない（2〜3文で1段落）。",
    "",
    "■無料版の制約（必ず守る）",
    "・この無料版では、結論としての『具体的な一手（何をいつ送る/どう動く）』は出さない。",
    "・代わりに『関係の流れの見立て』『分岐点の見抜き方（軽め）』までを提示する。",
    "",
 
    "■入力（材料）",
    `本人の気：${userPair}`,
    `相手の気：${partnerPair}`,
    "",
    "本人ミニ鑑定：",
    d.user_mini_reading ?? "",
    "",
    "相手ミニ鑑定：",
    d.partner_mini_reading ?? "",
    "",
    `関係性：${p.relation ?? "不明"}`,
    `直近の出来事：${p.recent_event ?? "不明"}`,
    "",
    "悩み（長文）：",
    c.free_text ?? "",
    "",
    "■出力の最後（必須）",
    "・最後は『あなた様の縁が静かに整いますように。占いばあやはいつでもあなたの幸せを願っております。』のように、短い祈りで締める。",
  ].join("\n");
}
