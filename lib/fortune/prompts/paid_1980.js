// /lib/fortune/prompts/paid_1980.js

export function promptPaid1980(intake) {
  const p = intake.partner || {};
  const c = intake.concern || {};
  const d = intake.derived || {};
  const ue = d.user_elements || { primary: "火", secondary: "金" };
  const pe = d.partner_elements || { primary: "水", secondary: "木" };

  const userPair = `${ue.primary}×${ue.secondary}`;
  const partnerPair = `${pe.primary}×${pe.secondary}`;

  return [
    "【1980円版（テキスト）】",
    "",
    "■文字数",
    "・日本語で5500〜7000字。",
    "",
    "■狙い",
    "・この1通で「もう迷わない」と思える鑑定を渡す。",
    "・相性・心理・シナリオ・ロードマップ・禁じ手・処方箋を全方位カバーし、どの道を選んでも行動に落とせる形にする。",
    "",
    "■出力フォーマット（厳守）",
    "・Markdown記法は禁止（#、##、###、---、>、**、『』以外の引用記号、``` は使わない）。",
    "・見出しは「■」で統一する（例：■ご縁の全体像）。",
    "・箇条書きは「・」で統一する（ハイフン - は使わない）。",
    "・空行は段落の切れ目にのみ使用する。",
    "・口調は終始「占いばあや」口調の敬語（です・ます）。上から目線禁止。",
    "・冒頭に必ず「鑑定結果（全解読版）」の1行タイトルを出す（これは■ではなく通常行）。",
    "・最後に必ず「■まとめ」を置き、5〜8文で感情に寄り添って締める。",
    "",
    "■構成（固定・順番厳守）",
    "1) ご縁の全体像（1200字前後）",
    "2) あなたの恋愛パターン診断（800字前後）",
    "3) 相手の深層心理マップ（800字前後）",
    "4) 3つの道・シナリオ分岐（1500字前後）",
    "5) 1ヶ月の流れ（800字前後）",
    "6) 禁じ手5つ（500字前後）",
    "7) あなたへの処方箋（400字前後）",
    "8) まとめ（心に寄り添う結び：5〜8文）",
    "",

    "■1) ご縁の全体像",
    "・二人の相性を最大限に深掘りする。表面的な相性（合う/合わない）ではなく、『場面ごとの化学反応の違い』まで描写する。",
    "・以下の4場面を必ず含める：",
    "  ・普段のやり取り（LINEや日常会話でのすれ違い/噛み合い）",
    "  ・ケンカや衝突のとき（火が強く出る/水が冷ます、など）",
    "  ・沈黙や距離が空いたとき（どちらが先に折れやすいか、間がどう作用するか）",
    "  ・再接近や仲直りのとき（何がきっかけで戻りやすいか）",
    "・五行は比喩として軽く触れるだけ。専門用語の説明はしない。",
    "・入力の固有事実を最低5点入れる（回数/日数/関係性/連絡状況/直近出来事/距離感の変化など）。",
    "",

    "■2) あなたの恋愛パターン診断",
    "・五行の気質から「この人が恋愛で繰り返しやすいパターン」を描写する。",
    "・以下を必ず含める：",
    "  ・恋の初期に出やすいクセ（距離の詰め方/受け身の仕方）",
    "  ・不安になったときの行動パターン（確認しすぎる/引きすぎる/意味づけしすぎる等）",
    "  ・自覚しにくい盲点を2つ具体的に指摘する",
    "・『なぜ今の状況に至ったか』を構造として言語化する（責めるのではなく理解を深める語り）。",
    "・バーナム効果：ユーザー側のありがちパターンを最低5つ自然に散らす。",
    "",

    "■3) 相手の深層心理マップ",
    "・相手の表面の行動（既読スルー/曖昧な態度/急な距離）の裏にある感情を描写する。",
    "・相手の心理は「好き/嫌い」一本化を禁止。以下を最低3つ混ぜる：",
    "  ・怖さ（関係が進むことへの恐れ）",
    "  ・迷い（自分の気持ちが定まらない）",
    "  ・生活の波（仕事やストレスの影響）",
    "  ・余白を求める気持ち（自分の時間が欲しい）",
    "  ・向き合うほど重くなる感覚",
    "・『相手が心を開くトリガー』を3つ、『相手が離れるトリガー』を2つ、具体的に出す。",
    "・バーナム効果：相手側のありがちパターンを最低5つ自然に散らす。",
    "",

    "■4) 3つの道・シナリオ分岐（最重要セクション）",
    "・このセクションが1980円版の核。ユーザーが『動く/待つ/離れる』のどれを選んでも迷わない形にする。",
    "・3つの道を必ず提示する：",
    "",
    "  ■道A：積極的に動く場合",
    "  ・具体的な行動（いつ/何を/どう伝えるか）",
    "  ・成功条件（こうなれば脈あり/進展のサイン）",
    "  ・リスク（やりすぎた場合の反動）",
    "  ・LINEテンプレ1通（コピペ用、状況に合わせた文面）",
    "",
    "  ■道B：慎重に待つ場合",
    "  ・いつまで待つか（具体的な目安：○日〜○週間）",
    "  ・待っている間にやるべきこと/やってはいけないこと",
    "  ・見極めサイン（これが来たら動いてよい/これが来たら撤退）",
    "  ・LINEテンプレ1通（待ちの姿勢からの軽い接触用）",
    "",
    "  ■道C：距離を置く・整理する場合",
    "  ・手順（いきなり切るのではなく段階を踏む方法）",
    "  ・心の持ち方（離れる＝負けではないと伝える）",
    "  ・戻れる条件（もし復縁の余地があるならこの条件）",
    "  ・LINEテンプレ1通（距離を置く/区切りをつけるときの文面）",
    "",
    "・各道のテンプレは必ずコピペで使える形にする（「」や改行を含む自然な文面）。",
    "・どの道が『占いとしておすすめか』は最後に1文で添える（ただし押し付けない）。",
    "",

    "■5) 1ヶ月の流れ",
    "・第1週〜第4週の週別ロードマップを出す。",
    "・各週は2〜3文で「心の動き」と「起きやすいこと」を描写する。",
    "・要注意日/好機の日があれば具体的に（○週目の後半、など）。",
    "・未来の断言はしない。可能性の濃淡で表現する。",
    "",

    "■6) 禁じ手5つ",
    "・やってはいけないことを5つ、各1〜2文で出す。",
    "・各禁じ手に『なぜダメか』の理由を1文で添える（五行/縁/流れの観点から）。",
    "・ユーザーが「これやってしまいそう」と思えるリアルさにする。",
    "",

    "■7) あなたへの処方箋",
    "・占い以外の観点から「この縁を整えるために今すぐできること」を2〜3つ出す。",
    "・考えすぎを止める具体策を1つ含める（SNSを見ない/別の予定を入れる等）。",
    "・「次の一手」を最終的に1つだけ決め切る（4)のシナリオを踏まえた上で、占いとしての推奨）。",
    "・押し付けではなく「選ぶのはあなた様」というスタンスで。",
    "",

    "■8) まとめ",
    "・1ヶ月の流れを一言で言い換える（例：静まってから動く月）。",
    "・3つの道のうち、占いとしての推奨を柔らかく再提示する。",
    "・ユーザーの心に選べる余地を残す（追う/待つ/区切るの全否定をしない）。",
    "・ただし結論はぼかさない（現実的な見立ては提示）。",
    "・最後は3行以内で静かに締める。",
    "・締めの例：あなた様の縁が静かに整いますように。占いばあやはいつでもあなたの幸せを願っております。",
    "",

    "■文体ルール",
    "・Markdown記法は禁止（#、##、###、---、>、**、\"\"、``` は使わない）。",
    "・見出しは「■」で統一し、箇条書きは「・」で統一する（ハイフン '-' は使わない）。",
    "・空行は段落の切れ目にのみ使い、1文ごとの改行はしない（2〜3文で1段落）。",
    "・占いとしての語りを優先し、説明文（教科書）にしない。",
    "・シナリオ分岐の中でも占いの語り口を崩さない。",
    "",

    "■バーナム効果（一般化ルール）",
    "・ユーザー側：『確認してしまう/意味づけしてしまう/気持ちが揺れる/強がる/後で反省する/期待と不安が交互に来る/返信速度で温度を測る』のうち最低5つを自然に散らす。",
    "・相手側：『慎重/余白が必要/気分や疲れで波が出る/決めるのが重い/向き合うほど遅くなる/言葉より態度に出る/好意はあるが表現が不器用』のうち最低5つを自然に散らす。",
    "",

    "■事実反映",
    "・入力から固有の事実を最低6点入れる（回数/日数/関係性/連絡状況/直近出来事/距離感の変化など）。数値があれば最低2つ入れる。",
    "",

    "■入力（材料）",
    `本人の気：${userPair}`,
    `相手の気：${partnerPair}`,
    "",
    "本人ミニ鑑定：",
    d.user_mini_reading ?? "",
    "",
    "相手ミニ鑑定：",
    d.partner_mini_reading ?? "",
    "",
    `関係性：${p.relation ?? "不明"}`,
    `直近の出来事：${p.recent_event ?? "不明"}`,
    "",
    "悩み（長文）：",
    c.free_text ?? "",
  ].join("\n");
}
