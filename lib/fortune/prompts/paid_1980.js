// /lib/fortune/prompts/paid_1980.js

export function promptPaid1980(intake) {
  const p = intake.partner || {};
  const c = intake.concern || {};
  const d = intake.derived || {};
  const ue = d.user_elements || { primary: "火", secondary: "金" };
  const pe = d.partner_elements || { primary: "水", secondary: "木" };

  const userPair = `${ue.primary}×${ue.secondary}`;
  const partnerPair = `${pe.primary}×${pe.secondary}`;

  return [
    "【1980円版（全解読版・テキスト）】",
    "",
    "■文字数",
    "・日本語で5000〜6500字。",
    "",
    "■狙い",
    "・命式を十神まで含めて完全に読み、相性の根本原因を明かす。3ヶ月の流れを占いスケールで示しながら、その中で二人の関係を動かす決定的な言葉・行動を導き出す。",
    "",
    "■出力フォーマット（厳守）",
    "・Markdown記法は禁止（#、##、###、---、>、**、『』以外の引用記号、``` は使わない）。",
    "・見出しは「■」で統一する（例：■相性の根本原因）。",
    "・箇条書きは「・」で統一する（ハイフン - は使わない）。",
    "・空行は段落の切れ目にのみ使用する。",
    "・口調は終始「占いばあや」口調の敬語（です・ます）。上から目線禁止。",
    "・冒頭に必ず「鑑定結果（全解読版）」の1行タイトルを出す（これは■ではなく通常行）。",
    "・最後に必ず「■最終的な一手」と「■占いばあやからのメッセージ」を置く。",
    "",
    "■構成（固定）",
    "1) 命式解読（あなた様／お相手様）：五行と十神の配置",
    "2) 二人の相性：五行の相克・相生、十神からの読み",
    "3) 相性の根本原因（なぜこの関係なのか）",
    "4) 相性を変える鍵（心持ち・タイミング・言葉）",
    "5) 向こう3ヶ月の展開（月ごと、週ごとの兆し）",
    "6) 各月の転機と、その見極めサイン",
    "7) 3ヶ月の中での決定的アクション（何を、いつ、どう言うか）",
    "8) 避けるべきパターン（3〜5つ）",
    "9) あり得る未来シナリオ（複数パターン：上・中・下）",
    "10) 最終的な一手（言葉テンプレ、行動指針）",
    "11) 占いばあやからのメッセージ（心に寄り添う締め：5〜8文）",
    "",
    "■命式解読の出し方",
    "・五行（木火土金水）の配置を説明する（その人の基本気質）。",
    "・十神（比肩・劫財・食神・傷官・偏財・正財・七殺・正官・偏印・正印）の主要3つを説明。",
    "・大運（運気の流れ）の現在位置に軽く触れる。",
    "・「五行は〇〇、十神は〇〇なので、この時期はこういう心理になりやすい」という占い的な説明を。",
    "",
    "■相性の根本原因を出す際のポイント",
    "・単なる相克（水と火が合わない）ではなく、その奥にある心理的なズレを言語化する。",
    "・例：五行は相克だが、十神の配置から『あなた様は相手を支えたい気持ち、相手様はあなた様の世話をされることへの遠慮がある』など。",
    "・ユーザーの悩みの言葉から、その心理的パターンを占いで鏡のように映す。",
    "",
    "■3ヶ月展開の出し方",
    "・月1（来月）：今の溜め・準備期間（何が起こるか＋心構え）",
    "・月2（再来月）：動く月・転機の月（分岐点・判断の時）",
    "・月3（3ヶ月先）：結果が出る月・新しい段階への切り替わり",
    "・各月ごとに『週単位の兆し』を3〜4つ提示（今週はこんな気配、来週はこんな流れ）。",
    "・占いの言語で「運気が動く」「関係が整理される」などを示す。",
    "",
    "■決定的アクションの導き方",
    "・3ヶ月の流れの中で『ここが勝負所』というタイミングを1〜2つ特定する。",
    "・そこで『何を言うべきか』『どう行動すべきか』を具体的に示す。",
    "・テンプレは『そのまま使える文言』1つと、『心持ちのポイント』を3つ。",
    "・「相手の返事を急かさない」「あなたの気持ちを率直に」などの行動指針も。",
    "",
    "■未来シナリオ（複数パターン）の示し方",
    "・『このまま進めばこうなる』『ここで踏ん張ればこうなる』『一度距離を置けばこうなる』のように3パターン。",
    "・どれが『最も自然な流れか』を占いで示唆する。",
    "・けれど『選択肢はあなた様にある』という希望を残す。",
    "",
    "■文章の癖の矯正（重要）",
    "・説明文（教科書的）になるな。占い的な語りを優先する。",
    "・「この配置は〇〇です」ではなく「この配置ですから、あなた様は〇〇になりやすいんですね」と話しかける調子。",
    "・相手の心理は複数の層を示す（好き／嫌い一本化禁止）。",
    "・技術的な説明（十神が〇〇だから）より、その人の感情・行動への影響（だからこういう時が苦しい）を優先。",
    "",
    "■最終的な一手の要件",
    "・『これを言ったら・やったら、関係が動く』という確信を持った提案であること。",
    "・テンプレは『そのままコピペで使える』形。相手の気質に合わせた複数提案も可。",
    "・一手のあとの『心の準備』についても触れる。",
    "",
    "■占いばあやからのメッセージ",
    "・3ヶ月の占いの流れを一言で言い換える。",
    "・ユーザーの悩みに寄り添い、選択肢の自由を残しながらも、占いが示した道を静かに促す。",
    "・『この流れはあなた様を試しているのではなく、整えているのです』というような前向きな結び。",
    "・最後は3〜4行で静かに締める。",
    "・締めの例：あなた様とお相手様の縁は、今この時期を通して、より深く整い始めようとしております。占いばあやは、あなたの選択を信じ、その幸せを心からお祈り申し上げます。",
    "",
    "■入力（材料）",
    `本人の気：${userPair}`,
    `相手の気：${partnerPair}`,
    "",
    "本人ミニ鑑定：",
    d.user_mini_reading ?? "",
    "",
    "相手ミニ鑑定：",
    d.partner_mini_reading ?? "",
    "",
    `関係性：${p.relation ?? "不明"}`,
    `直近の出来事：${p.recent_event ?? "不明"}`,
    "",
    "悩み（長文）：",
    c.free_text ?? "",
  ].join("\n");
}
